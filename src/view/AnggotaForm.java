    private AnggotaData getData(){
        String id = tfId.getText();
        String nama = tfNama.getText();
        String pekerjaan = tfPekerjaan.getText();
        String alamat = tfAlamat.getText();
        String no_telp = tfNo_telp.getText();
        
        // create Employee class
        AnggotaData e = new AnggotaData();
        e.setId(id);
        e.setNama(nama);
        e.setPekerjaan(pekerjaan);
        e.setAlamat(alamat);
        e.setNo_telp(no_telp);
        return e;
    }
