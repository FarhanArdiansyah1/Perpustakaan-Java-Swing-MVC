    private DefaultTableModel model;
    private void showData(){
        Object[] column = {"Id", "Nama", "Pekerjaan", "Alamat", "No. Telepon"};
        Object[][] data = new Object[service.getAnggota().size()][5];
        // value 3 means much column on table
        for (int i = 0; i < service.getAnggota().size(); i++) {
            AnggotaData e = service.getAnggota().get(i);
            Object[] arr_data = {e.getId(), e.getNama(), e.getPekerjaan(), e.getAlamat(), e.getNo_telp()};
            data[i] = arr_data;
        }
        model = new DefaultTableModel(data, column);
        tableData.setModel(model);
    }
